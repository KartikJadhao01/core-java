package core;

public class datatypes {
public static void main(String args[]) {
	byte b=120; //range	= -128 to 127
	System.out.println("byte value is:"+b);
	
	short s=32767; //range	= -32768 to 32767
	System.out.println("short value is:"+s);
	
	int i=2147483647; //range	= -2147483648 to 2147483647
	System.out.println("integer value is:"+i);
	
	long l=2147483647234488555l; //range	= -2147483648 to 2147483647
	System.out.println("long value is:"+l);
	
	double d =50.20;
	System.out.println("double value is:"+d);
	
	float f =50.20f;
	System.out.println("float value is:"+f);
	
	char c ='K';
	System.out.println("char value is:"+c);
	
	boolean bool =true;
	System.out.println("boolean value is:"+bool);
	
	String str =null;
	System.out.println("string value is:"+str);
}
}
