package core;

public class conditional_statements {
public static void main(String args[]) {
	//check whether number 89 is greater than 100 or not
	int num=89;
	if(num>100) {
		System.out.println("89 is greater than 100");
	}
	else {
		System.out.println("89 is less than 100");
	}
}
}
